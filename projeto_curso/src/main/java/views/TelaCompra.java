    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        int opcao = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja excluir a compra selecionada?");
        if(opcao == JOptionPane.YES_OPTION) {
            new CompraDAO().destroy(minhaCompra);
            
            // Limpa o formulário e atualiza a tabela de cadastrados
            limpar();
            preencherTabela();
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void tblCadastradosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCadastradosMouseClicked
        // TODO add your handling code here:
        int linha = tblCadastrados.getSelectedRow();
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        
        if(linha != -1) {
            String id = tblCadastrados.getValueAt(linha, 0).toString();
            
            // Constrói um objeto de Compra a partir do ID da linha selecionada
            minhaCompra = new CompraDAO().read(Integer.parseInt(id));
            
            // Preenche o formulário a partir do objeto retornado de Compra
            txtData.setText(sdf.format(minhaCompra.getData()));
            cmbFornecedor.setSelectedItem(minhaCompra.getFornecedor());
            cmbUsuario.setSelectedItem(minhaCompra.getUsuario());
        }
        
        btnEditar.setEnabled(true);
        btnExcluir.setEnabled(true);
        btnSalvar.setEnabled(false);
    }//GEN-LAST:event_tblCadastradosMouseClicked


