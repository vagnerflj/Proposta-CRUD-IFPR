    public void limpar() {
        txtData.setText("");
        tblCadastrados.clearSelection();
        cmbFornecedor.setSelectedIndex(-1);
        cmbUsuario.setSelectedIndex(-1);
        
        btnEditar.setEnabled(false);
        btnExcluir.setEnabled(false);
        btnSalvar.setEnabled(true);
    }
    
    public void preencherTabela() {
        DefaultTableModel dtm = (DefaultTableModel) tblCadastrados.getModel();
        dtm.setRowCount(0);
        CompraDAO dao = new CompraDAO();
        
        for(Compra c : dao.read()) {
            dtm.addRow(new Object[] {
                c.getId(),
                c.getData(),
                c.getFornecedor(),
                c.getUsuario()
            });
        }
    }
    
    public void preencherCmbFornecedor() {
        FornecedorDAO dao = new FornecedorDAO();
        for(Fornecedor f : dao.read()) {
            cmbFornecedor.addItem(f);
        }
    }
    
    public void preencherCmbUsuario() {
        UsuarioDAO dao = new UsuarioDAO();
        for(Usuario u : dao.read()) {
            cmbUsuario.addItem(u);
        }
    }

