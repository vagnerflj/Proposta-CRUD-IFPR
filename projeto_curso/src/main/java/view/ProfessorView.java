    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // Verifica se uma linha foi selecionada na tabela
        int linha = tblCadastrados1.getSelectedRow();
        if (linha != -1) {
            // Pega o ID do professor da tabela
            int professorId = (int) tblCadastrados1.getValueAt(linha, 2); // Supondo que o ID do professor esteja na coluna 2

            // Confirmação de exclusão
            int opcao = JOptionPane.showConfirmDialog(this, "Você tem certeza que deseja excluir este professor?", "Confirmar Exclusão", JOptionPane.YES_NO_OPTION);
            if (opcao == JOptionPane.YES_OPTION) {
                // Cria uma instância do ProfessorDAO e chama o método de exclusão
                ProfessorDAO professorDAO = new ProfessorDAO();
                professorDAO.excluir(professorId);

                // Exibe uma mensagem de sucesso
                JOptionPane.showMessageDialog(this, "Professor excluído com sucesso!");

                // Atualiza a tabela
                atualizarTabela();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um professor para excluir.", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

