    private void atualizarTabela() {
        // Cria uma instância do ProfessorDAO para buscar todos os professores
        ProfessorDAO professorDAO = new ProfessorDAO();
        List<Professor> professores = professorDAO.listar();

        // Cria o modelo da tabela
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblCadastrados1.getModel();
        model.setRowCount(0); // Limpa a tabela antes de adicionar as novas linhas

        // Itera sobre a lista de professores e adiciona cada um à tabela
        for (Professor professor : professores) {
            model.addRow(new Object[] {
                professor.getNome(),
                professor.getEmail(),
                professor.getId(),  // Caso precise exibir o ID do professor
            });
        }
    }
